SECTION TEXT
TESTE: MACRO
    ADD DOIS
    SUB DOIS
    END
TESTE2: MACRO
    ADD DOIS
    TESTE
    SUB DOIS
    END
INPUT OLD_DATA
LOAD OLD_DATA
L1: DIV DOIS
TESTE2
STORE  NEW_DATA
MULT DOIS
STORE TMP_DATA
LOAD OLD_DATA
SUB TMP_DATA
STORE TMP_DATA
OUTPUT TMP_DATA
COPY NEW_DATA, OLD_DATA
LOAD OLD_DATA
JMPP L1
STOP
SECTION DATA
DOIS: CONST 2
